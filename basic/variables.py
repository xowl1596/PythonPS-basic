#새 변수가 만들어 지는게 아니라 기존 변수에 새 값이 저장됩니다.
b = 10 #b라는 이름의 변수를 만들고 10 저장
b = 20 #새 변수를 만들기 않고 기존 b 변수에 20을 새로 저장
print(b) #> 20

