#데이터의 형태
#가장 기본적인 데이터는 정수, 실수, 문자 3가지로 나뉩니다.
#정수 : 1, 20, 50
#실수 : 1.0, 20.5, 123.456
#문자 : "1", "Hello", "123"

#정수와 실수는 서로 연산이 가능합니다.
print(5 + 1.5) #> 6.5

#하지만, 수와 문자는 대부분 연산이 불가능합니다.
print("123" + 4) #> 에러
#예외적으로, *는 문자와 숫자의 연산이나, 문자열을 반복시키는 연산자로 사용됩니다.
print("123" * 2) #> 123123

#이러한 문제를 해결하기 위해 파이썬에서는 데이터의 형태를 바꿀 수 있는 함수가 있습니다.
#데이터 형태를 바꾸는 것은 형변환이라고 부릅니다.
#형변환은 다음 함수를 이용합니다.
#int(데이터) -> 데이터를 정수로 바꿈
#float(데이터) -> 데이터를 실수로 바꿈
#str(데이터) -> 데이터를 문자로 바꿈

#형변환을 이용하면 다음과 같이 연산이 가능해 집니다.
a = "123"
print(int(a) + 100) #> 223

