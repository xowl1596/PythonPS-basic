#변수를 이용한 값 계산
#수가 들어있는 변수는 서로 연산이 가능합니다.
a = 1
b = 2
print(a + b) #> 3
#수를 이용한 연산은 다음과 같은 연산자가 있습니다.
# + : 두 수를 더함
print(1 + 1) #> 2
# - : 왼쪽 값에서 오른쪽 값을 뺌
print(5 - 3) #> 2
# * : 두 수를 곱합
print(2 * 5) #> 10
# / : 왼쪽 값을 오른쪽 값으로 나눔
print(5 / 2) #> 2.5
# ** : 제곱
print(5 ** 3) #> 125
# // : 왼쪽 값을 오른쪽 값으로 나눈 몫
print(5 // 2) #> 2
# % : 왼쪽 값을 오른쪽 값으로 나눈 나머지
print(5 // 2) #> 1

