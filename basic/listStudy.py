#리스트
#리스트는 여러 데이터를 순서대로 나열해서 묶어놓은 자료형 입니다.
#아래 코드는 리스트를 만드는 여러가지 예시입니다.
ls = [1,2,3,4]
ls2 = ['a','b','c','d']
ls3 = [1,'a','2',3]

#리스트는 문자열처럼 []을 이용해 특정 값을 가져올 수 있습니다.
ls = [1,2,3,4,5,6,7,8]
print(ls[3]) #> 4
#다만 [i:j]를 이용해 여러 값을 가져올 떄는 리스트로 가져옵니다.
print(ls[2:5]) #> [3,4,5]

