#리스트에 값 추가하기
#리스트에 값을 추가하는 방법은 append() 와 insert() 가 있습니다.
#append()는 리스트의 맨 마지막에 값을 추가합니다.
ls = [1,2,3]
ls.append(4)
print(ls) #> [1,2,3,4]

#insert()는 원하는 위치에 값을 삽입합니다.
ls = [1,2,3]
ls.insert(2,10) #ls의 2번째 위치에 10을 삽입
print(ls) #> [1,2,10,3]

