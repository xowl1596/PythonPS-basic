#리스트 안의 값 변경
#리스트의 각 값들은 다음과 같이 수정할 수 있습니다.
ls = [1,2,3,4]
ls[2] = 10 #ls의 2번째 값을 10으로 변경
print(ls) #> [1,2,10,4]

